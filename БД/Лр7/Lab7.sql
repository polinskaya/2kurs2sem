SELECT  	A1.AUDITORIUM_NAME AS [Номер аудитории], 
	A1.AUDITORIUM_CAPACITY AS [Вместимость],
	A1.AUDITORIUM_TYPE AS [Тип аудитории]
FROM AUDITORIUM A1 

SELECT 
	A1.AUDITORIUM_TYPE AS [Тип аудитории],
	A1.AUDITORIUM_CAPACITY AS [Максимальная вместимость для типа],
	A1.AUDITORIUM_NAME AS [Номера аудиторий такой вместимости] 
FROM AUDITORIUM A1 
WHERE AUDITORIUM_CAPACITY = (SELECT TOP(1) AUDITORIUM_CAPACITY FROM AUDITORIUM A2 
WHERE A1.AUDITORIUM_TYPE=A2.AUDITORIUM_TYPE ORDER BY AUDITORIUM_CAPACITY DESC)

