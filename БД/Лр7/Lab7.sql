SELECT  DISTINCT F.FACULTY AS [Факультет],
	P.PULPIT_NAME AS [Кафедра]	,
	PR.PROFESSION_NAME AS [Профессия]
FROM	 FACULTY AS F INNER JOIN PROFESSION AS PR ON PR.FACULTY=F.FACULTY INNER JOIN PULPIT AS P ON F.FACULTY=P.FACULTY	
WHERE	 PROFESSION_NAME LIKE '%технологи%'


