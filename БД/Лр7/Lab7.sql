-- Выбрать студентов, у которых оценка по предмету выше, чем у хоть кого
SELECT P1.IDSTUDENT AS [Студент], P1.NOTE AS [Оценка], P1.SUBJECTS AS [Предмет]
FROM PROGRESS AS P1 
WHERE NOTE>ANY(SELECT NOTE FROM PROGRESS AS P2 WHERE P1.SUBJECTS=P2.SUBJECTS)
